#include "libft.h"

char	*ft_strjoin_free(char *s1, int f1, char *s2, int f2)
{
	size_t		len;
	char		*str;
	size_t		i;
	size_t		j;

	i = 0;
	j = 0;
	if (!s1 || !s2)
		return (NULL);
	len = ft_strlen(s1) + ft_strlen(s2);
	if (!(str = (char*)ft_memalloc((len + 1) * sizeof(char))))
		return (NULL);
	while (s1[j])
		str[i++] = s1[j++];
	j = 0;
	while (s2[j])
		str[i++] = s2[j++];
	str[i] = '\0';
	if (f1 == 1)
		free(s1);
	if (f2 == 1)
		free(s2);
	return (str);
}
